    public void setfid(){
        String sql = "SELECT count(*) as count FROM FEE";
        int id=0;
        try {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                id = Integer.parseInt(rs.getString("count"));
             }
            id++;
            fid.setText(""+id);
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Execution failed");
        }
        
        
    }
    
