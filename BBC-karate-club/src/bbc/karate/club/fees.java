    public String getmonthnum(String a) throws ParseException{
        Date date = new SimpleDateFormat("MMMM").parse(a);
        Calendar cal = Calendar.getInstance();
        cal.setTime(date);
        int t=cal.get(Calendar.MONTH)+1;
        if(t<10)
            return 0+Integer.toString(t);
        else
            return Integer.toString(t);
    }
    
